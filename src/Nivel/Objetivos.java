package Nivel;

public class Objetivos {
	private int cantGemas;
    private int tipoGema;

    public Objetivos(int cantGemas, int tipoGema) {
        this.cantGemas = cantGemas;
        this.tipoGema = tipoGema;
    }

    public int getCantGemas() {
        return cantGemas;
    }

    public int getTipoGema() {
        return tipoGema;
    }
}
